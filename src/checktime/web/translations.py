# Dictionary containing all translations
TRANSLATIONS = {
    'en': EN,
    'es': ES,
}

def get_translation(key, lang='en'):
    """
    Get a translation for a given key and language.
    
    Args:
        key (str): The translation key to look up
        lang (str): The language code (e.g., 'en', 'es')
        
    Returns:
        str: The translated text, or the key if no translation is found
    """
    if lang not in TRANSLATIONS:
        lang = 'en'  # Default to English
        
    translations = TRANSLATIONS[lang]
    return translations.get(key, key)  # Return the key itself if no translation found

# Create a shorthand function for translations
def t(key, lang='en'):
    """Shorthand for get_translation."""
    return get_translation(key, lang) 